func removeBrowserSymlinks() {
	fmt.Println()
	infof("Removing gstack browser-dependent skill symlinks...")
	skillsDir := claudeSkillsDir()

	for _, name := range browserDependentSkills {
		target := filepath.Join(skillsDir, name)
		info, err := os.Lstat(target)
		if err != nil {
			continue
		}
		if info.Mode()&os.ModeSymlink != 0 {
			os.Remove(target)
			infof("  Removed: %s (replaced by agent-browser version)", name)
		}
	}
}

