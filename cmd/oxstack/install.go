// --- Helpers ---

// loadEnvFile reads a .env file and returns key=value pairs.
func loadEnvFile(path string) map[string]string {
	vars := make(map[string]string)
	data, err := os.ReadFile(path)
	if err != nil {
		return vars
	}
	for _, line := range strings.Split(string(data), "\n") {
		line = strings.TrimSpace(line)
		if line == "" || strings.HasPrefix(line, "#") {
			continue
		}
		if k, v, ok := strings.Cut(line, "="); ok {
			vars[strings.TrimSpace(k)] = strings.TrimSpace(v)
		}
	}
	return vars
}

// mergeJSON does a shallow merge of src into dst (src values win).
// For nested objects, it recurses.
func mergeJSON(dst, src map[string]any) {
	for k, sv := range src {
		if dv, ok := dst[k]; ok {
			// If both are maps, recurse
			if dm, dok := dv.(map[string]any); dok {
				if sm, sok := sv.(map[string]any); sok {
					mergeJSON(dm, sm)
					continue
				}
			}
		}
		dst[k] = sv
	}
}

func dirExists(path string) bool {
	info, err := os.Stat(path)
	return err == nil && info.IsDir()
}

func fileExists(path string) bool {
	info, err := os.Stat(path)
	return err == nil && !info.IsDir()
}
